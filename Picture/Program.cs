void PrintImage(int [,] image) 
{
    for (int i = 0; i < image.GetLength(0); i++) // идем по строчкам
    {
        for (int j = 0; j < image.GetLength(1); j++) // идем по колонкам
        {
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); // поднимаемся на строчку выше
        FillImage(row, col - 1); // идем в той же строке влево
        FillImage(row + 1, col); // идем вниз на строчку
        FillImage(row, col + 1); // идем в той же строке вправо
    }
}
